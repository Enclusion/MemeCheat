package me.joeleoli.memecheat.shape;

public class Vector2D {
    
    private double x;
    private double z;

    public Vector2D(double x, double z) {
        this.x = x;
        this.z = z;
    }

    public double getX() {
        return this.x;
    }

    public void setX(double x) {
        this.x = x;
    }

    public double getZ() {
        return this.z;
    }

    public void setZ(double z) {
        this.z = z;
    }
    
}